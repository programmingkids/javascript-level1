// ==
console.log( 3 == 3 );
console.log( 3 == '3' );

// !=
console.log( 3 != 4 );
console.log( 3 != '4' );

// ===
console.log( 3 === 3 );
console.log( '3' === '3' );

// !==
console.log( 3 !== 4 );
console.log( 3 !== '3' );

// >
console.log( 3 > 2 );
console.log( 3 > '2' );

// >=
console.log( 3 >= 2 );
console.log( 3 >= 3 );

// <
console.log( 3 < 4 );
console.log( 3 < '4' );

// <=
console.log( 3 <= 4 );
console.log( 3 <= 3 );
